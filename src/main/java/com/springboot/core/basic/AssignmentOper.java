package com.springboot.core.basic;

public class AssignmentOper {

	public static void main(String[] args) {
		
		int i=20;
		
		System.out.println(i);
		
		i=30;
		System.out.println(i);
		
		i+=5;
		System.out.println(i);
		
		i -=10;
		System.out.println(i);
		
		i /=5;
		System.out.println(i);
		
		i %=5;
		System.out.println(i);
		
		
	}
}
